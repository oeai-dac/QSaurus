    def open_templates_folder(self):
        # Definiere hier den Pfad zu deinen Vorlagedateien
        folder_path = self.plugin_dir + "./templates"  # z. B. "/home/user/qgis_templates" oder "C:/Users/username/Documents/QGIS_Templates"
    
        # Prüfe, ob der Pfad existiert
        if not os.path.exists(folder_path):
            QMessageBox.warning(None, "Ordner nicht gefunden", f"Der Ordner '{folder_path}' existiert nicht.")
            return
    
        # Öffne den Ordner im Dateiexplorer je nach Betriebssystem
        system_platform = platform.system()
        try:
            if system_platform == "Windows":
                os.startfile(folder_path)
            elif system_platform == "Darwin":  # macOS
                subprocess.Popen(["open", folder_path])
            else:  # Linux und andere
                subprocess.Popen(["xdg-open", folder_path])
        except Exception as e:
            QMessageBox.critical(None, "Fehler", f"Ordner konnte nicht geöffnet werden:\n{str(e)}")

