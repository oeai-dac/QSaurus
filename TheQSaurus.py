    def add_action(
        self,
        icon_path,
        text,
        callback,
        enabled_flag=True,
        add_to_menu=True,
        add_to_toolbar=True,
        status_tip=None,
        whats_this=None,
        parent=None):
        """Add a toolbar icon to the toolbar.

        :param icon_path: Path to the icon for this action. Can be a resource
            path (e.g. ':/plugins/foo/bar.png') or a normal file system path.
        :type icon_path: str

        :param text: Text that should be shown in menu items for this action.
        :type text: str

        :param callback: Function to be called when the action is triggered.
        :type callback: function

        :param enabled_flag: A flag indicating if the action should be enabled
            by default. Defaults to True.
        :type enabled_flag: bool

        :param add_to_menu: Flag indicating whether the action should also
            be added to the menu. Defaults to True.
        :type add_to_menu: bool

        :param add_to_toolbar: Flag indicating whether the action should also
            be added to the toolbar. Defaults to True.
        :type add_to_toolbar: bool

        :param status_tip: Optional text to show in a popup when mouse pointer
            hovers over the action.
        :type status_tip: str

        :param parent: Parent widget for the new action. Defaults None.
        :type parent: QWidget

        :param whats_this: Optional text to show in the status bar when the
            mouse pointer hovers over the action.

        :returns: The action that was created. Note that the action is also
            added to self.actions list.
        :rtype: QAction
        """

        icon = QIcon(icon_path)
        action = QAction(icon, text, parent)
        action.triggered.connect(callback)
        action.setEnabled(enabled_flag)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            # Adds plugin icon to Plugins toolbar
            self.iface.addToolBarIcon(action)

        if add_to_menu:
            self.iface.addPluginToMenu(
                self.menu,
                action)

        self.actions.append(action)

        return action

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        icon_path = ':/plugins/TheQSaurus/icon.png'
        self.add_action(
            icon_path,
            text=self.tr(u'TheQSaurus'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # will be set False in run()
        self.first_start = True


    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        for action in self.actions:
            self.iface.removePluginMenu(
                self.tr(u'&TheQSaurus'),
                action)
            self.iface.removeToolBarIcon(action)

    # WidgetTyp von gewählter Attributspalte wird auf WidgetType umgestellt
    def set_field_widget_type(self, layer, field_name, widget_type): 
        field_index = layer.fields().indexOf(field_name)
        if field_index == -1:
            self.iface.messageBar().pushWarning("TheQSaurus", f"Feld '{field_name}' nicht gefunden.")
            return

        config = {}
        widget_setup = QgsEditorWidgetSetup(widget_type, config)
        layer.setEditorWidgetSetup(field_index, widget_setup)
        layer.triggerRepaint()
        print(f"Widget-Typ von Feld '{field_name}' auf '{widget_type}' geändert.")

    # Ausgewählte Begriffe werden der Attributspalte hinzugefügt
    def set_value_map_widget(self, layer, field_name, value_map_dict): 
        field_index = layer.fields().indexOf(field_name)
        if field_index == -1:
            self.iface.messageBar().pushWarning("TheQSaurus", f"Feld '{field_name}' nicht gefunden.")
            return

        config = {
            'map': value_map_dict,
            'allowNull': True,  
            'useHtml': False  
        }

        widget_setup = QgsEditorWidgetSetup('ValueMap', config)
        layer.setEditorWidgetSetup(field_index, widget_setup)
        layer.triggerRepaint()
        print(f"ValueMap für Feld '{field_name}' gesetzt mit Werten: {value_map_dict}")


    # Befehle, die bei OK ausgeführt werden -> WidgetType ändern und mit Werten befüllen, Errorhandling falls Sachen nicht ausgwählt
    def on_ok_clicked(self):
        layer = self.dlg.eingabelayer.currentLayer()
        if not layer:
            self.iface.messageBar().pushWarning("TheQSaurus", "Kein Layer ausgewählt.")
            return

        field_name = self.dlg.eingabefeld.currentField()
        if not field_name:
            self.iface.messageBar().pushWarning("TheQSaurus", "Kein Feld ausgewählt.")
            return

        field_index = layer.fields().indexOf(field_name)
        if field_index == -1:
            self.iface.messageBar().pushWarning("TheQSaurus", f"Feld '{field_name}' nicht gefunden.")
            return

        selected_items = self.dlg.vocabsTree.selectedItems()
        if not selected_items:
            self.iface.messageBar().pushWarning("TheQSaurus", "Keine Auswahl getroffen.")
            return

        new_labels = [item.text(0) for item in selected_items]
        print("Neue Labels:", new_labels)

        # Werte an Widget übergeben
        # Aktuelle Widget-Konfiguration holen
        widget_setup = layer.editorWidgetSetup(field_index)
        config = widget_setup.config() if widget_setup.type() == "ValueMap" else {}

        # Alte ValueMap aus config holen oder neues dict
        old_value_map = config.get('map', {})

        # Neue Labels zu alten hinzufügen, ohne Duplikate
        for label in new_labels:
            if label not in old_value_map:
                old_value_map[label] = label

        # Aktualisierte ValueMap zurücksetzen
        self.set_value_map_widget(layer, field_name, old_value_map)
        self.iface.messageBar().pushMessage("TheQSaurus", "ValueMap erweitert.", level=Qgis.Success, duration=3)
        
        base_style_name = "qsaurus"

        # GPKG erkennen
        source = layer.dataProvider().dataSourceUri()
        path_part = source.split("|")[0]
        is_gpkg = path_part.lower().endswith(".gpkg")
        
        if is_gpkg and os.path.exists(path_part):
            # 1. Tabellenname (f_table_name) extrahieren
            table_name = None
            for part in source.split("|"):
                if part.startswith("layername="):
                    table_name = part.split("=")[1]
                    break
        
            if not table_name:
                print("⚠️ Tabellenname konnte nicht ermittelt werden!")
            else:
                print("🎯 Ziel-Tabellenname:", table_name)
        
                # 2. Styles im GPKG lesen
                conn = sqlite3.connect(path_part)
                cursor = conn.cursor()
        
                # Optional: sicherstellen, dass Tabelle existiert
                cursor.execute("SELECT name FROM sqlite_master WHERE type='table'")
                tables = [t[0] for t in cursor.fetchall()]
                if "layer_styles" not in tables:
                    print("ℹ️ Keine layer_styles-Tabelle gefunden. Stilname kann direkt verwendet werden.")
                    existing_style_names = []
                else:
                    cursor.execute("SELECT styleName FROM layer_styles WHERE f_table_name = ?", (table_name,))
                    existing_style_names = [row[0] for row in cursor.fetchall()]
                    print("🔍 Bereits vorhandene Style-Namen:", existing_style_names)
        
                conn.close()
                styles_before = set(existing_style_names)
        
                # 3. Neuen Stilnamen finden
                style_name = base_style_name
                counter = 1
                while style_name in existing_style_names:
                    style_name = f"{base_style_name}_{counter}"
                    counter += 1
        
                print("✅ Neuer Stilname:", style_name)
        
                # 4. Stil speichern
                success = layer.saveStyleToDatabase(
                    style_name,
                    '',  # Beschreibung
                    QgsMapLayer.StyleCategories(QgsMapLayer.StyleCategory.Forms),  # oder Forms, Symbology etc.
                    ''   # uiFileContent
                )
                
                conn = sqlite3.connect(path_part)
                cursor = conn.cursor()
                cursor.execute("SELECT styleName FROM layer_styles WHERE f_table_name = ?", (table_name,))
                styles_after = set(row[0] for row in cursor.fetchall())
                conn.close()
        
                if style_name in styles_after - styles_before:
                    self.iface.messageBar().pushMessage(f"✅ Stil '{style_name}' erfolgreich in GPKG gespeichert.", level=Qgis.Success, duration=3)
                    print(f"✅ Stil '{style_name}' erfolgreich in GPKG gespeichert.")
                else:
                    self.iface.messageBar().pushMessage("❌ Speichern des Stils fehlgeschlagen.", level=Qgis.Success, duration=3)
                    print("❌ Speichern des Stils fehlgeschlagen.")
        
        else:
            # Für Nicht-GPKG: Speicherpfad ermitteln
            layer_path = path_part
            directory = os.path.dirname(layer_path)
            base_filename = os.path.splitext(os.path.basename(layer_path))[0]
    
            style_filename = os.path.join(directory, f"{base_filename}_{base_style_name}.qml")
            counter = 1
            while os.path.exists(style_filename):
                style_filename = os.path.join(directory, f"{base_filename}_{base_style_name}_{counter}.qml")
                counter += 1
    
            # Speichern als .qml – nur "Forms"
            success = layer.saveNamedStyle(
                style_filename,
                QgsMapLayer.StyleCategories(QgsMapLayer.StyleCategory.Forms),
            )
    
            if not success:
                self.iface.messageBar().pushWarning("TheQSaurus", "Stil konnte nicht gespeichert werden.")
            else:
                self.iface.messageBar().pushMessage("TheQSaurus", f"Stil gespeichert unter {os.path.basename(style_filename)}.", level=Qgis.Success, duration=3)
    
