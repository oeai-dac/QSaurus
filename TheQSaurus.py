    # Suchfunktion
    def search_vocab_tree(self):
        search_text = self.dlg.searchLineEdit.text().strip().lower()
        if not search_text:
            self.iface.messageBar().pushWarning("TheQSaurus", "Bitte einen Suchbegriff eingeben.")
            return

        def search_items(item):
            match = False
            for i in range(item.childCount()):
                child = item.child(i)
                if search_items(child):
                    item.setExpanded(True)
                    match = True
            if search_text in item.text(0).lower():
                item.setBackground(0, QColor("yellow"))
                print(f"Treffer gefunden: {item.text(0)}")
                match = True
            else:
                item.setBackground(0, Qt.white)
            return match

        root_count = self.dlg.vocabsTree.topLevelItemCount()
        for i in range(root_count):
            top_item = self.dlg.vocabsTree.topLevelItem(i)
            search_items(top_item)

